        function [bool, featSupported] = supports(model)
            % [BOOL, FEATSUPPORTED] = SUPPORTS(MODEL)

            featUsed = model.getUsedLangFeatures();
            featSupported = SolverQNS.getFeatureSet();
            bool = SolverFeatureSet.supports(featSupported, featUsed);
        end
