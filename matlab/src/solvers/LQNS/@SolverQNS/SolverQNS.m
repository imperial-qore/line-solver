    methods (Static)
        function bool = isAvailable()
            %ISAVAILABLE Check if QNS is available on the system path
            bool = true;
            if ispc
                [~, ret] = dos('qns -V');
                if contains(ret, 'not recognized', 'IgnoreCase', true)
                    bool = false;
                    return;
                end
            else
                [~, ret] = unix('qns -V');
                if contains(ret, 'command not found', 'IgnoreCase', true)
                    bool = false;
                    return;
                end
            end
        end

        function options = defaultOptions()
            % OPTIONS = DEFAULTOPTIONS()

            options = SolverOptions('QNS');
            options.timespan = [Inf,Inf];
