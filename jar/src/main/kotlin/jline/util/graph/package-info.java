/**
 * Graph theory utilities and algorithms for network analysis.
 *
 * <p>This package provides graph-theoretic algorithms and data structures
 * for analyzing the topology and connectivity of queueing networks.
 * These utilities support routing analysis, network decomposition,
 * and structural property detection.
 *
 * <h2>Graph Representations</h2>
 * <ul>
 * <li>Adjacency matrix representation</li>
 * <li>Adjacency list structures</li>
 * <li>Weighted and unweighted graphs</li>
 * <li>Directed and undirected graph support</li>
 * </ul>
 *
 * <h2>Core Algorithms</h2>
 * <ul>
 * <li>Strongly connected component detection</li>
 * <li>Shortest path algorithms (Dijkstra, Floyd-Warshall)</li>
 * <li>Topological sorting</li>
 * <li>Cycle detection and enumeration</li>
 * <li>Minimum spanning tree construction</li>
 * </ul>
 *
 * <h2>Network Analysis</h2>
 * <ul>
 * <li>Routing matrix analysis</li>
 * <li>Network decomposition</li>
 * <li>Bottleneck identification</li>
 * <li>Critical path analysis</li>
 * <li>Flow conservation verification</li>
 * </ul>
 *
 * <h2>Applications</h2>
 * <ul>
 * <li>Queueing network topology analysis</li>
 * <li>Routing probability computation</li>
 * <li>Network simplification and aggregation</li>
 * <li>Performance bottleneck detection</li>
 * </ul>
 *
 * @see jline.util
 * @since LINE 2.0
 */
